async def classify_intent(message_text: str) -> str:
    try:
        response = await groq_client.chat.completions.create(
            model="llama-3.3-70b-versatile", #llama-3.3-70b-versatile #llama-3.1-8b-instant
            messages=[
                {"role": "system", "content": INTENT_CLASSIFICATION_PROMPT},
                {"role": "user", "content": message_text}
            ],
            response_format={"type": "json_object"},
            temperature=0,
            max_tokens=150,
        )
        raw = response.choices[0].message.content
        parsed = json.loads(raw)
        intent = parsed.get("intent", "").strip()

        if intent not in VALID_INTENTS:
            return "другое"
        return intent

    except (json.JSONDecodeError, KeyError):
        logger.warning("Groq вернул кривой JSON, переключаюсь на fallback-модель")
        return await classify_intent_fallback(message_text)

    except (APIStatusError, APIConnectionError, APITimeoutError) as e:
        logger.warning(f"Основная модель недоступна ({e}), переключаюсь на fallback-модель")
        return await classify_intent_fallback(message_text)

    except Exception as e:
        logger.warning(f"Groq classify failed непредвиденно: {e}")
        return "другое"


async def classify_intent_fallback(message_text: str) -> str:
    try:
        response = await groq_client.chat.completions.create(
            #"llama-3.1-8b-instant",
            model="llama-3.1-8b-instant",
            messages=[
                {"role": "system", "content": INTENT_CLASSIFICATION_PROMPT},
                {"role": "user", "content": message_text}
            ],
            response_format={"type": "json_object"},
            temperature=0,
            max_tokens=150,
        )
        parsed = json.loads(response.choices[0].message.content)
        intent = parsed.get("intent", "").strip()
        return intent if intent in VALID_INTENTS else "другое"

    except APIStatusError as e:
        if is_rate_limit_error(e):
            logger.warning(f"Fallback-модель тоже упёрлась в rate-limit: {e}")
            raise GroqRateLimitExhausted(retry_after=extract_retry_after(e)) from e
        logger.warning(f"Fallback-модель вернула ошибку не по rate-limit ({e}), отдаю 'другое'")
        return "другое"

    except Exception as e:
        logger.warning(f"Fallback-модель недоступна непредвиденно ({e}), отдаю 'другое'")
        return "другое"
